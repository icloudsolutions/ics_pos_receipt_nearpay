from odoo import fields, models


class ResConfigSettings(models.TransientModel):
    """Used to add new fields to the settings"""
    _inherit = "res.config.settings"

    nearpay_receipt_info = fields.Boolean(
        related='pos_config_id.nearpay_receipt_info',
        readonly=False,
        string="Show NearPay Info on Receipt",
        help="Display NearPay transaction details on the POS receipt"
    )